public class Numero {
    public static void main(String[] args) {
        int numero = 5;
        System.out.println(- numero);
        System.out.println(numero);


        numero = numero + 2;
        System.out.println(numero);
        numero += 2;
        System.out.println(numero);

        numero++;
        System.out.println(numero);
    }
}
